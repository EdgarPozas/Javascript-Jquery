//switch

var numero=2
//va a comparara
switch(numero){
	//los casos
	case 1:
	//si es 1
		console.log("es 1");
		//termina de comparar
		break;
	case 2:
	//si es 2
		console.log("es 2");
		//termina de comparar
		break;
	case 3:
	//si es 3
		console.log("es 3");
		//termina de comparar
		break;
	default:
	//no es ninguna
		console.log("no es ninguna");
		//termina de comparar
		break;
}